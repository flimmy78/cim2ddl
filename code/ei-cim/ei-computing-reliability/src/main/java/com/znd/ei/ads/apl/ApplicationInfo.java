package com.znd.ei.ads.apl;

public class ApplicationInfo {
	private String name;
	private String inputCC;
	private String outputCC;
	
	public String getInputCC() {
		return inputCC;
	}
	public void setInputCC(String inputCC) {
		this.inputCC = inputCC;
	}
	public String getOutputCC() {
		return outputCC;
	}
	public void setOutputCC(String outputCC) {
		this.outputCC = outputCC;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
